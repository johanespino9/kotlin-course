fun main() {
    // Operadores de comparacion
    // <,  >,  <=,  >=
    var numero1 = 13
    var numero2 = 12

    var comparacion1 = numero1 < numero2 // true , false
    var comparacion2 = numero2 > numero1
    var comparacion3 = numero2 <= numero1
    var comparacion4 = numero2 >= numero1
    var comparacion5 = numero2 <= numero1 && numero2 < numero1
    println(comparacion5)
}