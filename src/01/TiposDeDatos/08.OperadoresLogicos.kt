fun main() {
    // Operadores Logicos
    // Conjunción (&&) = Sera true cuando ambos sean verdaderos
    var booleano = true && true
    var booleano2 = true && false
    var booleano3 = false && true
    var booleano4 = false && false

    // Disyunción (||) = Sera true cuando uno de los elementos sea true o verdadero
    var disyuncion1 = true || true
    var disyuncion2 = true || false
    var disyuncion3 = false || true
    var disyuncion4 = false || false

    // Negación (!)
    var negacion = !true
    var negacion2 = !false
    println(negacion2)
}