fun main() {
    // Se necesita saber el promedio de las tres notas parciales
    // de un alumno. Siendo la primera nota 15, la
    // segunda nota 12 y la tercera 18.
    var nota1 = 15
    var nota2 = 12
    var nota3 = 18
    var promedio: Double = (nota1 + nota2 + nota3) / 3.0

    println(promedio)


    // Se pide hallar el precio final de un producto si
    // el descuento es del 15% y el precio del producto es 75.

    var precioProducto = 75
    var descuento = 0.15 // 15/100

    var precioDescuento = precioProducto * descuento

    var precioFinal = precioProducto - precioDescuento

    println(precioFinal)


    //

}